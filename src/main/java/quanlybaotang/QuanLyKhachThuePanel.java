    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // Kiểm tra dữ liệu người dùng nhập vào có hợp lệ không
        StringBuilder sb = new StringBuilder(); 
        DataValidator.validateEmpty(txtMaGianHang, sb, "Mã gian hàng cần phải nhập !");
        DataValidator.validateEmpty(txtDiaChi, sb, "Địa chỉ cần phải nhập !");
        DataValidator.validateEmpty(txtTenKhach, sb, "Tên khách cần phải nhập !");
        DataValidator.validateEmpty(txtThoiGianThue, sb, "Thời gian thuê cần phải nhập !");
        DataValidator.validateEmpty(txtTienDatCoc, sb, "Tiền đặt cọc cần phải nhập !");
        if (sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        
        try {
            KhachThue kt = new KhachThue();
            kt.setMaGianHangTrungBay(txtMaGianHang.getText());
            kt.setTen(txtTenKhach.getText());
            kt.setDiaChi(txtDiaChi.getText());
            kt.setThoiGianThue(Integer.parseInt(txtThoiGianThue.getText()));
            kt.setTienDatCoc(Float.parseFloat(txtTienDatCoc.getText()));
            
            KhachThueDao dao = new KhachThueDao();
           
            
                if(dao.insert(kt)){
                MessageDialogHelper.showMessageDialog(parentForm, "Thông tin khách thuê đã được lưu", "Thông báo");
                loadKhachThue();
                } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Không thể lưu thông tin khách thuê", "Thông báo");
                }
            
        } catch (Exception e) {
            e.printStackTrace(); // In ra cửa sổ nếu có lỗi
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaGianHang, sb, "Mã gian hàng không được để trống");
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        if(MessageDialogHelper.showConfirmDialog(parentForm,
                "Bạn có muốn xoá thông tin khách thuê không ?", "Hỏi") == JOptionPane.NO_OPTION){
            return;
        }
        try {   
            KhachThueDao dao = new KhachThueDao();
            if (dao.deleteByMaGianHang(txtMaGianHang.getText())){
                MessageDialogHelper.showMessageDialog(parentForm, "Xoá thông tin khách thuê thành công !", "Thông báo");
                btnThemActionPerformed(evt);
                loadKhachThue();
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Không xoá được thông tin do lỗi !", "Cảnh báo");
            }
        } catch (Exception e) {
           e.printStackTrace();
           MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(), "Lỗi");          
        }
    }//GEN-LAST:event_btnXoaActionPerformed

