    private void loadKhachThue(){
        try {
            KhachThueDao dao = new KhachThueDao();
            List<KhachThue> list = dao.findAll();
            tblModel.setRowCount(0);
            for (KhachThue kt : list) {
                tblModel.addRow(new Object[]{
                    kt.getMaGianHangTrungBay(), kt.getTen(), kt.getDiaChi(), kt.getThoiGianThue(), kt.getTienDatCoc()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace(); // In ra cửa sổ nếu có lỗi
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }
