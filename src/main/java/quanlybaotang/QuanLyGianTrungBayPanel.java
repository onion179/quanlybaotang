    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaGianHang, sb, "Mã gian hàng không được để trống");
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        if(MessageDialogHelper.showConfirmDialog(parentForm,
                "Bạn có muốn xoá gian hàng không ?", "Hỏi") == JOptionPane.NO_OPTION){
            return;
        }
        try {   
            GianHangDao dao = new GianHangDao();
            if (dao.delete(txtMaGianHang.getText())){
                MessageDialogHelper.showMessageDialog(parentForm, "Xoá gian hàng thành công !", "Thông báo");
                btnThemActionPerformed(evt);
                loadDataToTable();
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Không xoá được gian hàng do lỗi !", "Cảnh báo");
            }
        } catch (Exception e) {
           e.printStackTrace();
           MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(), "Lỗi");          
        }
    }//GEN-LAST:event_btnXoaActionPerformed

