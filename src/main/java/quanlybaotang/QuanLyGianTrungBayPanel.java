    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaGianHang, sb, "Mã gian hàng không được để trống");
        DataValidator.validateEmpty(txtDienTich, sb, "Diện tích không được để trống");
        DataValidator.validateEmpty(txtQuatLamMat, sb, "Quạt làm mát không được để trống");
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            GianHang gh = new GianHang();
            gh.setMaGianHangTrungBay(txtMaGianHang.getText());
            gh.setViTriGianTrungBay(txtViTriGianTrungBay.getText());
            gh.setDienTich(Integer.parseInt(txtDienTich.getText()));
            gh.setQuatLamMat(Integer.parseInt(txtQuatLamMat.getText()));
            gh.setBanGhe(Integer.parseInt(txtBanGhe.getText()));
            gh.setGianTrungBay(rdbTieuChuan.isSelected()?1:0);

            GianHangDao dao = new GianHangDao();
            if (dao.insert(gh)){
                MessageDialogHelper.showMessageDialog(parentForm, "Gian hàng đã được lưu !", "Thông báo");
                loadDataToTable();
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Không lưu được gian hàng do lỗi !", "Cảnh báo");
            }
        } catch (Exception e) {
           e.printStackTrace();
           MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(), "Lỗi");          
        }
    }//GEN-LAST:event_btnLuuActionPerformed

