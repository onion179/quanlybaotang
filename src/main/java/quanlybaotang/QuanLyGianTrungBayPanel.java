    private void findByMa() throws Exception{

        GianHangDao dao = new GianHangDao();
        tblModel.setRowCount(0);
        for(GianHang gh : dao.findByID(TimKiem)){
            Object rowDataObject[] = new Object[6];
            rowDataObject[0] = gh.getMaGianHangTrungBay();
            rowDataObject[1] = gh.getGianTrungBay() == 1 ? "Tiêu chuẩn" : "Cao cấp";
            rowDataObject[2] = gh.getDienTich();
            rowDataObject[3] = gh.getViTriGianTrungBay();
            rowDataObject[4] = gh.getQuatLamMat();
            rowDataObject[5] = gh.getBanGhe();
            tblModel.addRow(rowDataObject);
        }
   
    }
